class _FirstCorpSelectState extends State<FirstCorpSelect> {
  final FirebaseAuth _auth = FirebaseAuth.instance;

  TextEditingController searchController = new TextEditingController();
  late String filter;

  @override
  initState() {
    searchController.addListener(() {
      setState(() {
        filter = searchController.text;
      });
    });
  }

  @override
  void dispose() {
    searchController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    // final appState = Provider.of<AppState>(context, listen: false);

    return Scaffold(
        appBar: AppBar(
            backgroundColor: kPrimaryColor,
            title: Text('관심부대 선택',
                style: TextStyle(
                    color: Colors.white, fontWeight: FontWeight.bold))),
        body: new Column(
          children: <Widget>[
            new Padding(
              padding: new EdgeInsets.all(8.0),
              child: new TextField(
                controller: searchController,
                decoration: InputDecoration(
                  hintText: '관심부대 검색',
                  contentPadding: EdgeInsets.fromLTRB(20.0, 15.0, 20.0, 15.0),
                  border: OutlineInputBorder(
                      borderRadius: BorderRadius.circular(32.0)),
                ),
              ),
            ),
            new Expanded(
              child: new ListView.builder(
                itemCount: 533,
                itemBuilder: (context, index) {
                  // if filter is null or empty returns all data
                  return filter == null || filter == ""
                      ? ListTile(
                          title: Text(
                            '${contacts[index].fullName}',
                          ),
                          // subtitle: Text('${contacts[index].email}'),
                          leading: new CircleAvatar(
                              backgroundColor: kPrimaryColor,
                              child: Text(
                                  '${contacts[index].fullName.substring(0, 1)}',
                                  style: TextStyle(color: Colors.white))),
                          onTap: () {
                            // _onTapItem(context, contacts[index], appState);
                          })
                      : '${contacts[index].fullName}'
                              .toLowerCase()
                              .contains(filter.toLowerCase())
                          ? ListTile(
                              title: Text(
                                '${contacts[index].fullName}',
                                style: TextStyle(color: Colors.black),
                              ),
                              // subtitle: Text('${contacts[index].email}'),
                              leading: new CircleAvatar(
                                  backgroundColor: kPrimaryColor,
                                  child: Text(
                                    '${contacts[index].fullName.substring(0, 1)}',
                                    style: TextStyle(color: Colors.white),
                                  )),
                              // onTap: () => _onTapItem(context, contacts[index], appState),
                            )
                          : new Container();
                },
              ),
            ),
          ],
        ));
  }

  void _onTapItem(BuildContext context, Contact post, appState) {
    // Scaffold.of(context).showSnackBar(
    //     new SnackBar(content: new Text("Tap on " + ' - ' + post.fullName)));

    FirebaseFirestore.instance
        .collection('users')
        .doc(_auth.currentUser!.uid)
        .update({
      "corpType": post.fullName,
    });

    appState.login();
  }
}

